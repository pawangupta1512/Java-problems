package operators;

public class relationalOperators {
    public static void main(String[] args) {
        int currentStreak = 6;
        int targetStreak = 150;

        System.out.println(currentStreak == targetStreak);
        System.out.println(currentStreak != targetStreak);
        System.out.println(currentStreak > targetStreak);
        System.out.println(currentStreak < targetStreak);
        System.out.println(currentStreak >= targetStreak);
        System.out.println(currentStreak <= targetStreak);
    }
}
